# 转义字符
print("Hello\nWorld")
print("Hello\tWorld")

print("Helloooo\tWorld")

print("Hello\rWorld")

print("Hello\bWorld")

print("http:\\\\www.baidu.com")

print("老师说：\"大家好！\"")

# 使用原字符
print(r"Hello\nworld.")

# 注：最后一个字符不能是反斜杠
print("Hello\nWorld\\")
